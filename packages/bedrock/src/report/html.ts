export function frameworkLabel(tag: string): string {
  return FRAMEWORK_LABELS[tag] ?? tag;
}

const HTML_CONTEXT: HtmlReportContext = {
  defaultTitle: "AWS Bedrock Security Posture Report",
  eyebrow: "Security assessment · bedrock-security-mcp",
  scopeLine: "Assessed against OWASP LLM Top 10 (2025), OWASP Agentic Applications Top 10, NIST AI RMF, MITRE ATLAS.",
  footerLeft: `Generated by bedrock-security-mcp v${VERSION}`,
  footerRight: "OWASP LLM Top 10 (2025), OWASP Agentic Applications Top 10, NIST AI RMF, MITRE ATLAS",
  frameworkLabels: FRAMEWORK_LABELS,
  getRuleMetadata,
};

export function generateHtmlReport(
  findings: BedrockSecurityFinding[],
  options: {
    accountId?: string;
    region?: string;
    title?: string;
  } = {}
): string {
  return generateCoreHtmlReport(findings, options, HTML_CONTEXT);
}

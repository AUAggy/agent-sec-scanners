// Thin wrapper over the core HTML renderer: injects this pack's branding,
// framework labels, and metadata lookup. Output is byte-identical to
// bedrock-security-mcp v0.1.x EXCEPT the footer version, which now reflects the
// live package version instead of a hardcoded string (the golden suite
// normalizes the version line for this reason).

import { readFileSync } from "node:fs";
import {
  generateHtmlReport as generateCoreHtmlReport,
  type HtmlReportContext,
} from "@miaggy/core";
import type { BedrockSecurityFinding } from "../types.js";
import { getRuleMetadata } from "../rules/catalog.js";

/** The pack's own version, read from package.json so the report footer never
 * drifts from the published version. Resolves in both src (tests) and dist. */
const VERSION: string = (() => {
  try {
    return JSON.parse(readFileSync(new URL("../../package.json", import.meta.url), "utf8")).version;
  } catch {
    return "0.0.0";
  }
})();

